package model.bean;

/**
 *
 * @author luizk
 */
public class CidadeBean {
    private String nome;
    private String cep;
    private String uf;
    private int codigo;

    public CidadeBean(){
    
    }
    public CidadeBean(int codigo, String nome) {
        this.codigo = codigo;
        this.nome = nome;
    }
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    public String getUf() {
        return uf;
    }

    public void setUf(String uf) {
        this.uf = uf;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }
    
    
}
