    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (txtPrecoTotal.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para continuar!");
            txtPrecoTotal.requestFocus();
        } else if (txtCodFormapag.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para continuar!");
            txtCodFormapag.requestFocus();

        } else if (txtData.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para continuar!");
            txtData.requestFocus();
        } else {

            if (flag == 1) {
                mod.setTotal(Integer.parseInt(txtPrecoTotal.getText()));
                mod.setData_(txtData.getText());
                mod.setCod_formapag(Integer.parseInt(txtCodFormapag.getText()));

                mod.setCod_cl(Integer.parseInt(txtCodCliente.getText()));
                control.Salvar(mod);

                txtCliente.setText("");
                txtCodCliente.setText("");
                txtCod.setText("");
                txtCliente.setText("");
                txtCod.setText("");
                txtCodCliente.setText("");
                txtData.setText("");
                txtPrecoTotal.setText("");

                btnBuscarCliente.setEnabled(false);
                

                txtPrecoTotal.setEnabled(false);
                txtData.setEnabled(false);
                txtCodFormapag.setEnabled(false);
                btnBuscarForma.setEnabled(false);
                txtCliente.setEnabled(false);

                btnSalvar.setEnabled(false);
                btnCancelar.setEnabled(false);
                btnNovo.setEnabled(true);
                ktableCliente.setEnabled(false);
                ktableCompra.setEnabled(true);
                preencherTabelaCliente("select * from cliente order by nome");
        preencherTabelaCompra("select * from compra order by codigo");
        preencherTabelaFormapag("select * from forma_pagamento order by descricao");

            } else if (flag != 1) {
                mod.setTotal(Integer.parseInt(txtPrecoTotal.getText()));
                mod.setData_(txtData.getText());
                mod.setCod_formapag(Integer.parseInt(txtCodFormapag.getText()));
                mod.setCodigo(Integer.parseInt(txtCod.getText()));
                mod.setCod_cl(Integer.parseInt(txtCodCliente.getText()));
                control.Editar(mod);

                txtCliente.setText("");
                txtCodCliente.setText("");
                txtCod.setText("");
                txtCliente.setText("");
                txtCod.setText("");
                txtCodCliente.setText("");
                txtData.setText("");
                txtPrecoTotal.setText("");

                
                txtCliente.setEnabled(false);
                txtCod.setEnabled(false);
                btnBuscarCliente.setEnabled(false);
               

                txtPrecoTotal.setEnabled(false);
                txtData.setEnabled(false);
                txtCodFormapag.setEnabled(false);
                btnBuscarForma.setEnabled(false);
                txtCliente.setEnabled(false);

                btnSalvar.setEnabled(false);
                btnCancelar.setEnabled(false);
                btnNovo.setEnabled(true);
                ktableCliente.setEnabled(false);
                ktableCompra.setEnabled(true);
                preencherTabelaCliente("select * from cliente order by nome");
        preencherTabelaCompra("select * from compra order by codigo");
        preencherTabelaFormapag("select * from forma_pagamento order by descricao");

            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

