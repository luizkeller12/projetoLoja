    private void btnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarActionPerformed
        // TODO add your handling code here:

        int resposta = 0;
        resposta = JOptionPane.showConfirmDialog(rootPane, "Realmente deseja Apagar?");
        if (resposta == JOptionPane.YES_OPTION) {
            mod.setCodigo(Integer.parseInt(txtCod.getText()));
            control.Apagar(mod);
            btnEditar.setEnabled(false);
            btnApagar.setEnabled(false);
            btnCancelar.setEnabled(false);
            txtCpf.setEnabled(false);
            txtNome.setEnabled(false);
            kComboBoxCid.setEnabled(false);
            txtEnd.setEnabled(false);

            txtCod.setText("");
            txtCpf.setText("");
            txtEnd.setText("");
            txtNome.setText("");
            txtPesq.setText("");
            txtTel.setText("");
            //preencherTabela("select * from cliente order by nome");
            btnNovo.setEnabled(true);
            btnBuscar.setEnabled(true);
            txtPesq.setEnabled(true);

        }
    }//GEN-LAST:event_btnApagarActionPerformed

