    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (txtCpf.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para continuar!");
            txtCpf.requestFocus();
        } else if (txtEnd.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para continuar!");
            txtEnd.requestFocus();

        } else {

            if (flag == 1) {
                mod.setCpf(Integer.parseInt(txtCpf.getText()));
                mod.setEndereco(txtEnd.getText());
                mod.setTelefone(Integer.parseInt(txtTel.getText()));
                mod.setNome(txtCpf.getText());
                mod.setCod_cidade(Integer.parseInt(kComboBoxCid.getSelectedItem().toString()));
                control.Salvar(mod);

                txtCod.setText("");
                txtCpf.setText("");
                txtEnd.setText("");
                txtNome.setText("");
                txtPesq.setText("");
                txtTel.setText("");
                btnBuscar.setEnabled(true);
                txtPesq.setEnabled(true);
                txtCpf.setEnabled(false);
                txtEnd.setEnabled(false);
                txtTel.setEnabled(false);
                
                kComboBoxCid.setEnabled(false);
                btnSalvar.setEnabled(false);
                btnCancelar.setEnabled(false);
                ktableCliente.setEnabled(true);
                //preencherTabela("select * from cidade order by nome");
            } else {
                mod.setCpf(Integer.parseInt(txtCpf.getText()));
                mod.setEndereco(txtEnd.getText());
                mod.setTelefone(Integer.parseInt(txtTel.getText()));
                mod.setNome(txtCpf.getText());
                mod.setCod_cidade(Integer.parseInt(kComboBoxCid.getSelectedItem().toString()));
                mod.setCodigo(Integer.parseInt(txtCod.getText()));
                control.Editar(mod);
                txtCpf.setEnabled(false);
                txtEnd.setEnabled(false);
                ktableCliente.setEnabled(false);
                txtNome.setEnabled(true);
                txtTel.setEnabled(true);
                txtCod.setText("");
                txtCpf.setText("");
                txtEnd.setText("");
                txtNome.setText("");
                txtPesq.setText("");
                txtTel.setText("");
                btnBuscar.setEnabled(true);
                txtPesq.setEnabled(true);
                txtCpf.setEnabled(false);
                txtEnd.setEnabled(false);
                txtTel.setEnabled(false);
                
                kComboBoxCid.setEnabled(false);
                btnSalvar.setEnabled(false);
                btnCancelar.setEnabled(false);
                ktableCliente.setEnabled(true);
                //preencherTabela("select * from cidade order by nome");
            }
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

