    private void btnFInalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFInalizarActionPerformed
        // TODO add your handling code here:
        
        if ((txtQuantidade.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "preencha todos os campos para continuar!");
            txtQuantidade.requestFocus();
            
        } else if (txtValor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "preencha todos os campos para continuar!");
            txtValor.requestFocus();
            
        } else if (txtCodCompra.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selecione a compra para continuar!");
            ktableCompra.requestFocus();
            
        } else if (txtCodProd.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selecione o produto para continuar!!");
            ktableProduto.requestFocus();            
            
        } else {
            flag = 2;
            int result;
            result = Integer.parseInt(txtValor.getText()) * Integer.parseInt(txtQuantidade.getText());
            txtVf.setText(String.valueOf(result));
            btnSalvar.setEnabled(true);
            ktableCompra.setEnabled(false);
            ktableProduto.setEnabled(false);
            txtProduto.setEnabled(false);
            txtQuantidade.setEnabled(false);
            txtValor.setEnabled(false);
            txtVf.setEnabled(false);
            btnFInalizar.setEnabled(false);
            btnCancelar.setEnabled(true);
        }
        
    }//GEN-LAST:event_btnFInalizarActionPerformed

