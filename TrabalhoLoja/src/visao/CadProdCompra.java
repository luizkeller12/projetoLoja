    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        if (txtQuantidade.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para continuar!");
            txtQuantidade.requestFocus();
        } else if (txtValor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para continuar!");
            txtValor.requestFocus();
        } else {
            
            if (flag == 1) {
                JOptionPane.showMessageDialog(null, "Compra não finalizada!");
                btnFInalizar.requestFocus();
                
            } else if (flag != 1) {
                mod.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
                mod.setValor(Integer.parseInt(txtValor.getText()));
                mod.setCod_prod(Integer.parseInt(txtCodProd.getText()));
                mod.setCod_compra(Integer.parseInt(txtCodCompra.getText()));
                control.Salvar(mod);
                
                txtCodCompra.setText("");
                txtCodProd.setText("");
                txtProduto.setText("");
                txtQuantidade.setText("");
                txtVf.setText("");
                txtValor.setText("");
                
                btnBuscarProd.setEnabled(false);
                
                btnNovo.setEnabled(!false);
                txtValor.setEnabled(!true);
                txtQuantidade.setEnabled(!true);
                txtProduto.setEnabled(!true);
                btnBuscarProd.setEnabled(!true);
                btnFInalizar.setEnabled(!true);
                
                btnSalvar.setEnabled(false);
                btnCancelar.setEnabled(false);
                btnNovo.setEnabled(true);
                ktableProduto.setEnabled(false);
                ktableCompra.setEnabled(false);
                preencherTabelaProduto("select * from produto order by nome");
                preencherTabelaCompra("select * from compra order by codigo");
                
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

