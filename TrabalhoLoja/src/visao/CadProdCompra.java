    public void preencherTabelaCompra(String Sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"total", "data_", "codigo"};
        conex.conexao();
        conex.executaSql(Sql);
        
        try {
            conex.rs.first();
            do {
                dados.add(new Object[]{
                    conex.rs.getInt("total"),
                    conex.rs.getString("data_"),
                    conex.rs.getInt("codigo"),});
            } while (conex.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Busque outro codigo de compra na tabela" + ex);
            
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        
        ktableCompra.setModel(modelo);
        ktableCompra.getColumnModel().getColumn(0).setPreferredWidth(90);
        ktableCompra.getColumnModel().getColumn(0).setResizable(false);
        ktableCompra.getColumnModel().getColumn(1).setPreferredWidth(90);
        ktableCompra.getColumnModel().getColumn(1).setResizable(false);
        ktableCompra.getColumnModel().getColumn(2).setPreferredWidth(90);
        
        ktableCompra.getTableHeader().setReorderingAllowed(false);
        
        ktableCompra.setAutoResizeMode(ktableCompra.AUTO_RESIZE_OFF);
        ktableCompra.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
    }
    
    public void preencherTabelaProduto(String Sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"nome", "codigo"};
        conex.conexao();
        conex.executaSql(Sql);
        
        try {
            conex.rs.first();
            do {
                dados.add(new Object[]{conex.rs.getString("nome"), conex.rs.getInt("codigo")});
            } while (conex.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Busque outro produto na tabela");
            
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        
        ktableProduto.setModel(modelo);
        ktableProduto.getColumnModel().getColumn(0).setPreferredWidth(400);
        ktableProduto.getColumnModel().getColumn(0).setResizable(false);
        ktableProduto.getColumnModel().getColumn(1).setPreferredWidth(110);
        ktableProduto.getColumnModel().getColumn(1).setResizable(false);
        
        ktableProduto.setAutoResizeMode(ktableProduto.AUTO_RESIZE_OFF);
        ktableProduto.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
    }

