    public void preencherTabelaProdCompra(String Sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"quantidade", "valor", "cod_prod", "cod_compra"};
        conex.conexao();                  
        conex.executaSql(Sql);

        try {
            conex.rs.first();
            do {
                dados.add(new Object[]{
                    conex.rs.getInt("quantidade"),
                    conex.rs.getString("valor"),
                    conex.rs.getInt("cod_prod"),
                    conex.rs.getInt("cod_compra"),                   
                });
            } while (conex.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Busque outro codigo de compra na tabela" + ex);

        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);

        ktableProdCompra.setModel(modelo);
        ktableProdCompra.getColumnModel().getColumn(0).setPreferredWidth(170);
        ktableProdCompra.getColumnModel().getColumn(0).setResizable(false);
        ktableProdCompra.getColumnModel().getColumn(1).setPreferredWidth(180);
        ktableProdCompra.getColumnModel().getColumn(1).setResizable(false);
        ktableProdCompra.getColumnModel().getColumn(2).setPreferredWidth(95);
        ktableProdCompra.getColumnModel().getColumn(2).setResizable(false);
        ktableProdCompra.getColumnModel().getColumn(3).setPreferredWidth(95);
        ktableProdCompra.getColumnModel().getColumn(3).setResizable(false);
       

        ktableProdCompra.getTableHeader().setReorderingAllowed(false);

        ktableProdCompra.setAutoResizeMode(ktableProdCompra.AUTO_RESIZE_OFF);
        ktableProdCompra.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
    }

