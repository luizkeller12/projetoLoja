    private void btnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarActionPerformed
        // TODO add your handling code here:
        int resposta = 0;
        resposta = JOptionPane.showConfirmDialog(rootPane, "Realmente deseja Apagar?");
        if (resposta == JOptionPane.YES_OPTION) {
            mod.setCod_prod(Integer.parseInt(lblCodProd.getText()));
            mod.setCod_compra(Integer.parseInt(lblCodCompra.getText()));
            control.Apagar(mod);
            
                    
        
            lblCodCompra.setText("");
            lblValor.setText("");
            lblCodProd.setText("");
            lvlQuantidade.setText("");
            
            preencherTabelaProdCompra("select * from prod_compra order by valor");
         

        }
    }//GEN-LAST:event_btnApagarActionPerformed

